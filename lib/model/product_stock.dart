class Product {
  final String id;
  String name;
  String category;
  double price;
  double rating;
  String imageUrl;
  String description;

  Product({ 
    required this.id,
    required this.name,
    required this.category,
    required this.price,
    required this.rating,
    required this.imageUrl,
    required this.description,
  });
}

